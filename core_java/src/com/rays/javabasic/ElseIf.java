package com.rays.javabasic;

public class ElseIf {
	public static void main(String[] args) {

		int a = 15;
		int b = 15;
		
		
		if (a > b) {
			System.out.println("maximum no is = " + a);
		} else {
			System.out.println("maximum no is = " + b);
		}
		if (a<b) {
			System.out.println("minimum no is="+a);
		}else {
			System.out.println("minimum no is ="+b);
		}
	}
}
