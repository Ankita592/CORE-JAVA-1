package com.rays.javabasic;

public class ForLoop {
	
       public static void main(String[] args) {
    	   
    	   int a = 15;
    	   for ( int i = 1; i <= a; i++) {
    		   System.out.println(i+ "hello world");
    		   
    	   }for (int b=15; b>=1 ; b--) {
    		   System.out.println(b);
    	   }
    		   
    	   
	
}
}
