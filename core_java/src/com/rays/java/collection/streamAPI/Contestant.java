package com.rays.java.collection.streamAPI;

public class Contestant {
	
	public String phone;
	public String name;
	
	public Contestant() {
		
	}

	public Contestant(String phone, String name) {
		super();
		this.phone = phone;
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public String getName() {
		return name;
	}

	
}
    
