package com.rays.javabasicExercise;

public class HarmonicSeries {
	
	public static void main(String[] args) {
		 
		 int n = 10 ;
		
		 System.out.println("  " + "  ");
		 
		
		  for (int i = 1; i <= 10; i++) {
			  System.out.print("1/" + i);
			  
		  if(i != n) {
			  
			  System.out.print(" + ");
			  
		  }
		  }
			  System.out.println();
		  
	}
	}



